#include <stdio.h>
#include <stdlib.h>

float suma(float a, float b)
{
    float suma;
    suma = a + b;
    return suma;
}

float resta(float a, float b)
{
    float resta;
    resta = a - b;
    return resta;
}

float division(float a, float b)
{
    float division;
    division = a / b;
    return division;
}

float multiplicacion(float a, float b)
{
    float multiplicacion;
    multiplicacion = a * b;
    return multiplicacion;
}

